close all; clear; clc

t = [ 0,10,15,20,22.5,30];
v = [ 0,227.04, 362.78, 517.35, 602.97, 901.67 ];

A = [ 1, t(2), t(2)^2, t(2)^3; ...
      1, t(3), t(3)^2, t(3)^3; ...
      1, t(4), t(4)^2, t(4)^3; ...
      1, t(5), t(5)^2, t(5)^3];
b = [v(2); v(3); v(4); v(5)];

c = A\b;

v_int = @(t) c(1)  + c(2)*t + c(3)*t.^2 + c(4)*t.^3;

t_int = linspace(t(1), t(end), 100);

plot(t,v,'bo',t_int, v_int(t_int), 'r-')
